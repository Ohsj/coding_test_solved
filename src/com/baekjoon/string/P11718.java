package com.baekjoon.string;

import java.util.*;

/**
 * https://www.acmicpc.net/problem/11718
 */

public class P11718 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while(sc.hasNextLine()) {
            String s = sc.nextLine();
            System.out.println(s);
        }
        sc.close();
    }
}
